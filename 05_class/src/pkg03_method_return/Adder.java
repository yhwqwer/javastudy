package pkg03_method_return;

public class Adder {

  /*
   * 메소드의 반환타입
   * 1. void   : 메소드의 호출 결과 값은 없다.
   * 2. int    : 메소드의 호출 결과 값의 타입은 int 이다.
   * 3. String : 메소드의 호출 결과 값의 타입은 String 이다.
   * ...
   */
  
  int plus(int number1, int number2) {
    return number1 + number2; // return 메소드결과값;
  }
  
}
